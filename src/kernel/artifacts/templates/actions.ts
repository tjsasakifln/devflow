    "## Pre-requisites",
    "- [ ] requirements.md completo e revisado",
    "- [ ] roadmap.md aprovado em architecture review",
    "- [ ] test-plan.md definido",
    "- [ ] Constitution check passando (C1-C8)",
    "",
    "## Action List",
    "",
    "### T001 — <!-- título da ação -->",
    "",
