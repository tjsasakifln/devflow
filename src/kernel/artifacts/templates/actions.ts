    "| **Status** | [ ] Pendente / [~] Em progresso / [X] Concluída / [!] Bloqueada |",
    "",
    "### T002 — <!-- título da ação -->",
    "",
    "| Campo | Valor |",
    "|-------|-------|",
    "| **Alvo exato** | `src/<!-- path -->.ts` |",
    "| **Camada** | `<!-- Domínio / Aplicação / Infra / Interface -->` |",
    "| **Contrato esperado** | `<!-- assinatura -->` |",
